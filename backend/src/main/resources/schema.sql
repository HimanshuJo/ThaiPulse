DROP TABLE IF EXISTS bangkok_news;
DROP TABLE IF EXISTS pattaya_news;
DROP TABLE IF EXISTS phuket_news;
DROP TABLE IF EXISTS news;
DROP TABLE IF EXISTS bangkok_scoop_news;
DROP TABLE IF EXISTS thailand_island_news;

CREATE TABLE bangkok_news (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(500) NOT NULL,
    source VARCHAR(100000),
    link VARCHAR(500) UNIQUE,
    image VARCHAR(500),
    PUBLISHED_DATE VARCHAR(500)
);

CREATE TABLE pattaya_news (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(500) NOT NULL,
    description VARCHAR(100000),
    link VARCHAR(500) UNIQUE,
    image_url VARCHAR(500),
    PUBLISHED_DATE VARCHAR(500)
);

CREATE TABLE phuket_news (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(500) NOT NULL,
    description VARCHAR(100000),
    link VARCHAR(500) UNIQUE,
    image_url VARCHAR(500),
    PUBLISHED_DATE VARCHAR(500)
);

CREATE TABLE news (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(500) NOT NULL,
    source VARCHAR(255),
    link VARCHAR(500) UNIQUE,
    image VARCHAR(500)
);

CREATE TABLE bangkok_scoop_news (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(500) NOT NULL,
    source VARCHAR(255),
    link VARCHAR(500) UNIQUE,
    image VARCHAR(500)
);

CREATE TABLE thailand_island_news (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(500) NOT NULL,
    source VARCHAR(255),
    link VARCHAR(500) UNIQUE,
    image VARCHAR(500)
);